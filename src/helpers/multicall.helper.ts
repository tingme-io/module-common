const getContract = (abi: any, address: string, network_id: number) => {
  const simpleRpcProvider = new ethers.providers.StaticJsonRpcProvider(
    rpc[network_id],
  );
  return new ethers.Contract(address, abi, simpleRpcProvider);
};

interface Call {
  address: string; // Address of the contract
  name: string; // Function name on the contract (example: balanceOf)
  params?: any[]; // Function params
}

const multicall = async <T = any>(
  abi: any[],
  calls: Call[],
  network_id: number,
): Promise<T> => {
  try {
    const multi = getContract(
      multiCall,
      contracts.multiCall[network_id],
      network_id,
    );
    const itf = new ethers.utils.Interface(abi);

    const calldata = calls.map((call) => [
      call.address.toLowerCase(),
      itf.encodeFunctionData(call.name, call.params),
    ]);
    const { returnData } = await multi.aggregate(calldata);

    const res = returnData.map((call, i) =>
      itf.decodeFunctionResult(calls[i].name, call),
    );

    return res;
  } catch (error) {
    throw new Error(error);
  }
};

